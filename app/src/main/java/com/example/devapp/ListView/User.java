package com.example.devapp.ListView;

public class User {

    String name, msg, msgTime, phoneNum, country;
    int imageId;

    public User(String name, String msg, String msgTime, String phoneNum, String country, int imageId) {
        this.name = name;
        this.msg = msg;
        this.msgTime = msgTime;
        this.phoneNum = phoneNum;
        this.country = country;
        this.imageId = imageId;
    }
}
